export type TranslationKeys = keyof typeof T.en;

export function useT(): (key: TranslationKeys, params?: Record<string, string | number>) => string {
  const lang = useUserPreferencesStore((s) => s.preferences.language) as SupportedLang;
  const dict = T[lang] ?? T.en;
  return (key: TranslationKeys, params?: Record<string, string | number>) => {
    const raw = (dict as Record<string, string>)[key] ?? (T.en as Record<string, string>)[key] ?? key;
    if (!params) return raw;
    return Object.entries(params).reduce(
      (acc, [k, v]) => acc.replace(`{${k}}`, String(v)),
      raw
    );
  };
}
