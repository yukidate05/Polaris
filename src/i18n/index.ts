export type TranslationKeys = keyof typeof T.en;

export function useT(): (key: TranslationKeys) => string {
  const lang = useUserPreferencesStore((s) => s.preferences.language) as SupportedLang;
  const dict = T[lang] ?? T.en;
  return (key: TranslationKeys) => (dict as Record<string, string>)[key] ?? (T.en as Record<string, string>)[key] ?? key;
}
